package fr.univavignon.pokedex.api;

/**
 * Représente les différentes équipes de Pokémon dans le jeu Pokémon Go.
 * Chaque équipe est associée à un nom spécifique.
 */
public enum Team {

	/** L'équipe Mystic (Bleue), connue pour sa sagesse et sa recherche de la vérité. */
	MYSTIC,

	/** L'équipe Instinct (Jaune), axée sur l'instinct et la confiance. */
	INSTINCT,

	/** L'équipe Valor (Rouge), motivée par la passion et la force. */
	RED,

	/** L'équipe Valor (Rouge), également appelée Valor (même équipe que RED). */
	VALOR
}
